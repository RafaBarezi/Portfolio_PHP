<?php

// Introducao aos arrays

 
/*

Array  um conjunto de elementos contidosem uma estrutura onde cada elemento possui uma chave que o identifica.

No php arrays no possuem tipo.

A qualquer momento podemos adicionar ou alterar elementos.

Existem 3 formas de trabalhar com arrays:

>>> indexados - onde os elementos possuem ndice numrico no negativo 

>>> associativos - onde cada elemento est vinculado a uma chave

>>> multidimencionais - estruturas que possuem arrays como elementos

*/

$a = array(1,2,3);

# print_r  especfico para imprimir array
# Arrays sempre se iniciam no 0
print_r($a);  

?>